package ANY;

public class Student {
	private String name="bear";
	private int grade;
	private boolean graduated;
	
	public String Name() {
		return name;
	}
	public void Name(String name) {
		this.name = name;
	}
	public int Grade() {
		return grade;
	}
	public void Grade(int grade) {
		this.grade = grade;
	}
	public boolean Graduated() {
		return graduated;
	}
	public void Graduated(boolean graduated) {
		this.graduated = graduated;
	}
}
